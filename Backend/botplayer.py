    def get_worthy_continent_from_set(self, curr_dist, setupmode=False):
        obtainable_continents = []

        for cont_name, cont_data in self.gs.map.conts.items():
            cont_territories = set(cont_data['trtys'])
            # A continent is obtainable if any territory in curr_dist belongs to it
            if cont_territories.intersection(curr_dist):
                obtainable_continents.append(cont_name)

        if not obtainable_continents:
            return None

        best_continent = None
        best_score = -1

        for cont_name in obtainable_continents:
            score = self.get_continental_worth_points(cont_name, curr_dist, setupmode)

            if score > best_score:
                best_score = score
                best_continent = cont_name

        return best_continent, best_score

    # Function Used to determine the worthiness of a unconquered territory
    def get_continental_worth_points(self, cont_name, curr_dist, setupmode):
        # Easiness
        NumF = 0
        NumN = 0
        for trty in self.gs.map.conts[cont_name]['trtys']:
            if trty in curr_dist:
                mul = 1 if self.gs.map.territories[trty].isCapital else 0
                mulc = 1 if self.gs.map.territories[trty].isCity else 0
                NumF += self.gs.map.territories[trty].troops + (mul * 5) + (mulc * 3)
            else:
                mul = 1 if self.gs.map.territories[trty].isCapital else 0
                mulc = 1 if self.gs.map.territories[trty].isCity else 0
                NumN += self.gs.map.territories[trty].troops + (mul * 5) + (mulc * 3)
        if NumN == 0:
            if setupmode:
                return math.log(1+NumF)**2 * self.gs.map.conts[cont_name]['bonus']
            else:
                return -100
        easiness = math.log(1 + NumF/NumN)

        # Bonus
        bonus = self.gs.map.conts[cont_name]['bonus']

        score = ( easiness * bonus )
        return score

