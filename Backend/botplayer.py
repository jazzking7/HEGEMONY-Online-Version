    def get_best_defendable_territory(self, tid_list):
        """
        Given a list of TIDs, find the best defendable territory within
        the worthy continent based on fewest enemies and chokepoints within radius 2.
        """
        # Step 1: Find the worthy continent
        worthy_cont, _ = self.get_worthy_continent_from_set(tid_list, True)
        print(worthy_cont)
        if worthy_cont is None:
            return None

        cont_tids = set(self.gs.map.conts[worthy_cont]['trtys'])

        # Step 2: Filter tid_list to only those in the worthy continent
        candidates = [tid for tid in tid_list if tid in cont_tids]
        if not candidates:
            return None

        # Step 3: Score each candidate
        best_tid = None
        best_score = float('inf')  # lower is better

        for tid in candidates:
            nearby = self.get_neighbors_within_radius(tid, 2)

            # Count enemies: TIDs in radius-2 that are NOT in our tid_list
            tid_set = set(tid_list)
            enemy_count = sum(1 for n in nearby if n not in tid_set)

            # Count chokepoints: territories in radius-2 that are chokepoints
            chokepoint_count = sum(1 for n in nearby if self.is_chokepoint(n))

            # Lower score = more defendable
            score = enemy_count + (chokepoint_count)**(2.5)

            if score < best_score:
                best_score = score
                best_tid = tid

        return best_tid
    
    def get_neighbors_within_radius(self, start_tid, radius):
        visited = {start_tid}
        frontier = {start_tid}
        for _ in range(radius):
            next_frontier = set()
            for tid in frontier:
                for neighbor in self.gs.map.territories[tid].neighbors:
                    if neighbor not in visited:
                        visited.add(neighbor)
                        next_frontier.add(neighbor)
            frontier = next_frontier
        visited.discard(start_tid)
        return visited

    def is_chokepoint(self, tid):
        territory_cont = None
        for cont_name, cont_data in self.gs.map.conts.items():
            if tid in cont_data['trtys']:
                territory_cont = set(cont_data['trtys'])
                break
        if territory_cont is None:
            return False
        return any(n not in territory_cont for n in self.gs.map.territories[tid].neighbors)
    
    def get_best_territories_to_build(self, N, build_type):
        """
        Returns top N territories to build upon, ranked by strategic score.
        build_type: "leyline", "hall", "city", "logistic_nexus", "megacity", "infrastructure"
        """

        # ------------------------------------------------------------------ #
        #  SLOT FILTERING
        # ------------------------------------------------------------------ #

        def get_valid_territories():
            valid = []
            for tid in self.territories:
                t = self.gs.map.territories[tid]
                if t.isDeadZone:
                    continue
                if build_type in ("leyline", "hall"):
                    if not t.isCapital and not t.isHall and not t.isLeyline:
                        valid.append(tid)
                elif build_type in ("city", "logistic_nexus"):
                    if not t.isCity and not t.isBureau and not t.isTransportcenter:
                        valid.append(tid)
                elif build_type == "megacity":
                    if t.isCity and not t.isMegacity:
                        valid.append(tid)
                elif build_type == "infrastructure":
                    return []  # global, no territory needed
            return valid

        valid = get_valid_territories()
        if not valid:
            return []

        player_set = set(self.territories)

        # ------------------------------------------------------------------ #
        #  TERRITORIAL IMPORTANCE (own territories)
        # ------------------------------------------------------------------ #

        importance, _ = self.get_trty_importance(self)

        # ------------------------------------------------------------------ #
        #  CONTINENT PRESENCE SCORE
        #  presence = (player_troops_in_cont / total_troops_in_cont)
        #           * sum of importance of player-controlled territories in cont
        # ------------------------------------------------------------------ #

        def continent_presence(cont_name):
            cont_data   = self.gs.map.conts[cont_name]
            cont_tids   = cont_data['trtys']
            player_troops_in_cont = 0
            total_troops_in_cont  = 0
            valuable_score        = 0.0

            for tid in cont_tids:
                t = self.gs.map.territories[tid]
                total_troops_in_cont += t.troops
                if tid in player_set:
                    player_troops_in_cont += t.troops
                    valuable_score        += importance.get(tid, 0)

            if total_troops_in_cont == 0:
                return 0.0

            return (player_troops_in_cont / total_troops_in_cont) * valuable_score

        # ------------------------------------------------------------------ #
        #  CONTINENT CAPTURE LIKELIHOOD
        #  How easy is it to capture the rest of the continent?
        #  = player troops present vs enemy troops present
        # ------------------------------------------------------------------ #

        def continent_capture_likelihood(cont_name):
            cont_data    = self.gs.map.conts[cont_name]
            cont_tids    = cont_data['trtys']
            player_troops = 0
            enemy_troops  = 0

            for tid in cont_tids:
                t = self.gs.map.territories[tid]
                if tid in player_set:
                    player_troops += t.troops
                else:
                    enemy_troops += t.troops

            if enemy_troops == 0:
                return float('inf')  # fully controlled
            return player_troops / enemy_troops

        # ------------------------------------------------------------------ #
        #  PRECOMPUTE CONTINENT SCORES
        # ------------------------------------------------------------------ #

        cont_scores = {}
        for cont_name in self.gs.map.conts:
            presence    = continent_presence(cont_name)
            likelihood  = continent_capture_likelihood(cont_name)
            cont_scores[cont_name] = (presence, likelihood)

        def get_continent(tid):
            for cont_name, cont_data in self.gs.map.conts.items():
                if tid in cont_data['trtys']:
                    return cont_name
            return None

        # ------------------------------------------------------------------ #
        #  FULLY CONTROLLED CONTINENTS
        # ------------------------------------------------------------------ #

        controlled_conts = set()
        for cont_name, cont_data in self.gs.map.conts.items():
            if all(t in player_set for t in cont_data['trtys']):
                controlled_conts.add(cont_name)

        # ------------------------------------------------------------------ #
        #  ENEMY PRESENCE IN RADIUS 2
        # ------------------------------------------------------------------ #

        def enemy_troops_radius(tid):
            radius2      = self.get_neighbors_within_radius(tid, 2)
            enemy_troops = 0
            for n in radius2:
                if n not in player_set:
                    enemy_troops += self.gs.map.territories[n].troops
            return enemy_troops

        # ------------------------------------------------------------------ #
        #  CHOKEPOINT COUNT IN RADIUS 1 (light tiebreaker)
        # ------------------------------------------------------------------ #

        def chokepoint_count(tid):
            radius1 = self.get_neighbors_within_radius(tid, 1)
            return sum(1 for n in radius1 if self.is_chokepoint(n))

        # ------------------------------------------------------------------ #
        #  SCORE EACH VALID TERRITORY
        # ------------------------------------------------------------------ #

        scored = []

        for tid in valid:
            cont_name = get_continent(tid)

            if cont_name:
                presence, likelihood = cont_scores[cont_name]
            else:
                presence, likelihood = 0.0, 0.0

            # Tier multiplier
            if cont_name in controlled_conts:
                # Already fully control this continent — highest priority
                tier = 2.0
            elif likelihood > 1.0:
                # More player troops than enemy — likely to capture
                tier = 1.5
            else:
                tier = 1.0

            # Base score: presence in continent * tier
            base_score = presence * tier

            # Secondary: capture likelihood bonus
            capture_bonus = min(likelihood, 5.0) * 10  # cap to avoid inf dominating

            # Tertiary: fewer enemies around = better
            enemy_penalty = enemy_troops_radius(tid) * 0.1

            # Tiebreaker: fewer chokepoints nearby = better
            choke_penalty = chokepoint_count(tid) * 0.5

            final_score = base_score + capture_bonus - enemy_penalty - choke_penalty
            scored.append((tid, final_score))

        scored.sort(key=lambda x: x[1], reverse=True)
        return [tid for tid, _ in scored[:N]]
    
    def get_best_territories_to_defend(self, tid_list):
        tid_set = set(tid_list)

        # -------------------------
        # 1. Controlled continents
        # -------------------------
        controlled_conts = {}
        cont_troop_totals = {}

        for cont_name, cont_data in self.gs.map.conts.items():
            cont_tids = cont_data['trtys']
            if all(t in tid_set for t in cont_tids):
                # Find chokepoints within this continent
                chokepoints = [t for t in cont_tids if self.is_chokepoint(t)]
                controlled_conts[cont_name] = chokepoints
                cont_troop_totals[cont_name] = sum(
                    self.gs.map.territories[t].troops for t in cont_tids
                )

        # Sort controlled_conts by total troops descending
        controlled_conts = dict(
            sorted(controlled_conts.items(),
                key=lambda x: cont_troop_totals[x[0]],
                reverse=True)
        )

        # -------------------------
        # 2. Capital(s)
        # -------------------------
        my_capital_tid = None
        other_capitals = []

        for tid in tid_list:
            territory = self.gs.map.territories[tid]
            if territory.isCapital:
                if territory.name == self.capital:
                    my_capital_tid = tid
                else:
                    other_capitals.append(tid)

        # -------------------------
        # 3. Cities
        # -------------------------
        cities = [tid for tid in tid_list if self.gs.map.territories[tid].isCity]

        # -------------------------
        # 4. Other special categories
        # -------------------------
        megacities       = [tid for tid in tid_list if self.gs.map.territories[tid].isMegacity]
        transport_centers = [tid for tid in tid_list if self.gs.map.territories[tid].isTransportcenter]
        forts            = [tid for tid in tid_list if self.gs.map.territories[tid].isFort]
        halls            = [tid for tid in tid_list if self.gs.map.territories[tid].isHall]
        leylines         = [tid for tid in tid_list if self.gs.map.territories[tid].isLeyline]
        bureaus          = [tid for tid in tid_list if self.gs.map.territories[tid].isBureau]

        # -------------------------
        # 5. High value TIDs scoring
        # -------------------------
        scored = []
        for tid in tid_list:
            t = self.gs.map.territories[tid]
            points = 0

            if t.isMegacity:        points += 50
            if t.isTransportcenter: points += 40
            if t.isHall:            points += 50
            if t.isLeyline:         points += 15
            if t.isBureau:          points += 15
            if self.is_chokepoint(tid): points += 15

            if points > 0:
                scored.append((tid, points))

        scored.sort(key=lambda x: x[1], reverse=True)
        high_value_tids = [tid for tid, _ in scored]

        # -------------------------
        # 6. Build and return result
        # -------------------------
        return {
            "controlled_conts":   controlled_conts,
            "capital":            my_capital_tid,
            "capitals":           other_capitals,
            "cities":             cities,
            "megacities":         megacities,
            "transport_centers":  transport_centers,
            "forts":              forts,
            "halls":              halls,
            "leylines":           leylines,
            "bureaus":            bureaus,
            "high_value_tids":    high_value_tids,
        }
    
    def execute_upgrade_plan(self, upgrade_result):
        """
        Executes purchases from get_upgrade_plan result.
        Handles both 'upgrade' and 'abandon' status (both have a plan).
        """
        status = upgrade_result.get('status')
        if status not in ('upgrade', 'abandon'):
            return

        plan = upgrade_result.get('plan', []) if status == 'upgrade' \
            else upgrade_result.get('best_plan', [])
        if not plan:
            return

        CONVERSION = {
            2: 3,  3: 5,  4: 7,  5: 10, 6: 13, 7: 16,
            8: 19, 9: 23, 10: 28, 11: 33, 12: 38,
            13: 44, 14: 52, 15: 60
        }

        for item in plan:
            name  = item['item']
            count = item['count']
            cost  = item['cost']

            # ---------------------------------------------------------- #
            #  TROOP CONVERSION
            # ---------------------------------------------------------- #
            if name == 'troop_conversion':
                # count = troops gained, find the star cost
                star_cost = next(
                    (s for s, t in CONVERSION.items() if t == count),
                    None
                )
                if star_cost is None or self.stars < star_cost:
                    continue
                self.build('troop_conversion', None, star_cost)

            # ---------------------------------------------------------- #
            #  INFRASTRUCTURE (global, no territory)
            # ---------------------------------------------------------- #
            elif name == 'infrastructure':
                for _ in range(count):
                    result = self.build('infrastructure', None)
                    if not result:
                        break

            # ---------------------------------------------------------- #
            #  TERRITORY-BASED BUILDS
            # ---------------------------------------------------------- #
            else:
                tids = self.get_best_territories_to_build(count, name)
                for tid in tids:
                    result = self.build(name, tid)
                    if not result:
                        break

