    def get_trty_importance(self, player):
        importance = {}
        held_continents = {}

        total_troops = player.total_troops

        # Step 1: Calculate base importance for each territory
        for tid in player.territories:
            t = self.gs.map.territories[tid]
            points = 0

            if t.isMegacity:        points += 60
            if t.isTransportcenter: points += 50
            if t.isHall:            points += 50
            if t.isLeyline:         points += 15
            if t.isBureau:          points += 15
            if t.isCapital:         points += 25
            if t.isCity:            points += 20

            if player.name == self.name:
                if self.get_mission_related_tids():
                    if tid in self.get_mission_related_tids():
                        points += 50

            if total_troops > 0 and t.troops > total_troops * 0.01:
                points += (t.troops / total_troops) * 100

            importance[tid] = points

        # Step 2: Find fully controlled continents
        player_set = set(player.territories)

        for cont_name, cont_data in self.gs.map.conts.items():
            cont_tids = set(cont_data["trtys"])
            if not cont_tids.issubset(player_set):
                continue

            # Continent is fully controlled — calculate its importance
            cont_importance = 0
            troop_presence = 0
            chokepoints = []

            for tid in cont_data["trtys"]:
                t = self.gs.map.territories[tid]
                cont_importance += importance[tid]

                # Troop percentage contribution
                if total_troops > 0:
                    troop_presence += (t.troops / total_troops) * 100

                # Check if territory is a chokepoint (neighbor outside continent)
                for neighbor_tid in t.neighbors:
                    if neighbor_tid not in cont_tids:
                        chokepoints.append(tid)
                        break

            agenda_importance = 0
            if player.name == self.name:
                if self.get_mission_related_cont():
                    if cont_name == self.get_mission_related_cont():
                        agenda_importance = 1
                
            cont_importance = cont_importance + (cont_data["bonus"]) + troop_presence + agenda_importance*100
            held_continents[cont_name] = {
                "importance": cont_importance,
                "chokepoints": chokepoints
            }

        # Sort held_continents by importance descending
        held_continents = dict(
            sorted(held_continents.items(), key=lambda x: x[1]["importance"], reverse=True)
        )

        # Step 3: Update importance based on held_continents
        # Multipliers: 1st -> 1.5, 2nd -> 1.3, 3rd -> 1.1, rest -> 1.0
        multipliers = [1.7, 1.5, 1.3, 1.1]

        for i, (cont_name, cont_data) in enumerate(held_continents.items()):
            multiplier = multipliers[i] if i < len(multipliers) else 1.0
            cont_tids = set(self.gs.map.conts[cont_name]["trtys"])
            chokepoint_set = set(cont_data["chokepoints"])

            for tid in cont_tids:
                if tid in importance:
                    if tid in chokepoint_set:
                        importance[tid] += 5
                    importance[tid] *= multiplier

        return importance, held_continents
    
