    def get_best_defendable_territory(self, tid_list):
        """
        Given a list of TIDs, find the best defendable territory within
        the worthy continent based on fewest enemies and chokepoints within radius 2.
        """
        # Step 1: Find the worthy continent
        worthy_cont, _ = self.get_worthy_continent_from_set(tid_list, True)
        if worthy_cont is None:
            return None

        cont_tids = set(self.gs.map.conts[worthy_cont]['trtys'])

        # Step 2: Filter tid_list to only those in the worthy continent
        candidates = [tid for tid in tid_list if tid in cont_tids]
        if not candidates:
            return None

        # Step 3: Score each candidate
        best_tid = None
        best_score = float('inf')  # lower is better

        for tid in candidates:
            nearby = self.get_neighbors_within_radius(tid, 2)

            # Count enemies: TIDs in radius-2 that are NOT in our tid_list
            tid_set = set(tid_list)
            enemy_count = sum(1 for n in nearby if n not in tid_set)

            # Count chokepoints: territories in radius-2 that are chokepoints
            chokepoint_count = sum(1 for n in nearby if self.is_chokepoint(n))

            # Lower score = more defendable
            score = enemy_count + (chokepoint_count)**(2.5)

            if score < best_score:
                best_score = score
                best_tid = tid

        return best_tid
    
    def get_neighbors_within_radius(self, start_tid, radius):
        visited = {start_tid}
        frontier = {start_tid}
        for _ in range(radius):
            next_frontier = set()
            for tid in frontier:
                for neighbor in self.gs.map.territories[tid].neighbors:
                    if neighbor not in visited:
                        visited.add(neighbor)
                        next_frontier.add(neighbor)
            frontier = next_frontier
        visited.discard(start_tid)
        return visited

    def is_chokepoint(self, tid):
        territory_cont = None
        for cont_name, cont_data in self.gs.map.conts.items():
            if tid in cont_data['trtys']:
                territory_cont = set(cont_data['trtys'])
                break
        if territory_cont is None:
            return False
        return any(n not in territory_cont for n in self.gs.map.territories[tid].neighbors)
    

    def get_best_territories_to_build(self, tid_list, N, build_type):
        """
        Returns top N territories to build upon, ranked by strategic score.
        """
        # Step 1: Filter valid territories
        valid = []
        if build_type == "cities":
            valid = [tid for tid in tid_list if not self.gs.map.territories[tid].isCity] if build_type == "cities" else list(tid_list)
        if not valid:
            return []

        tid_set = set(tid_list)

        # --- Determine Priority Tiers ---

        # Tier 1: Continents the player already fully controls
        controlled_conts = set()
        for cont_name, cont_data in self.gs.map.conts.items():
            if all(t in tid_set for t in cont_data['trtys']):
                controlled_conts.add(cont_name)

        # Tier 2: Most worthy potential continent (existing logic)
        worthy_cont, _ = self.get_worthy_continent_from_set(tid_list, True)

        # Map each TID to its continent
        def get_continent(tid):
            for cont_name, cont_data in self.gs.map.conts.items():
                if tid in cont_data['trtys']:
                    return cont_name
            return None

        # --- Score each valid territory ---
        scored = []

        for tid in valid:
            territory = self.gs.map.territories[tid]
            cont_name = get_continent(tid)

            # -- Proximity modifier for chokepoints (weighted by distance) --
            radius1 = self.get_neighbors_within_radius(tid, 1)
            radius2 = self.get_neighbors_within_radius(tid, 2)
            ring2_only = radius2 - radius1  # neighbors exactly 2 steps away

            chokepoint_penalty = 0
            for n in radius1:
                if self.is_chokepoint(n):
                    chokepoint_penalty += 2.0   # closer chokepoints hurt more
            for n in ring2_only:
                if self.is_chokepoint(n):
                    chokepoint_penalty += 1.0   # further chokepoints hurt less

            # -- Friendly troops on this territory (higher = better) --
            friendly_troops = territory.troops

            # -- Enemy troops in surrounding 2 layers (higher = worse) --
            enemy_troops = 0
            for n in radius2:
                if n not in tid_set:
                    enemy_troops += self.gs.map.territories[n].troops

            # Raw defendability score
            raw_score = ((friendly_troops * 2) / (max(enemy_troops, 1) * 1.5)) * 100

            # -- Apply tier multiplier --
            if cont_name in controlled_conts:
                multiplier = 2.0    # Tier 1: already control this continent
            elif cont_name == worthy_cont:
                multiplier = 1.5    # Tier 2: most worthy potential continent
            else:
                multiplier = 1.0    # Tier 3: everything else

            final_score = raw_score * multiplier - (chokepoint_penalty ** 2)
            scored.append((tid, final_score))

        # Sort descending by score, return top N TIDs
        scored.sort(key=lambda x: x[1], reverse=True)
        return [tid for tid, _ in scored[:N]]
    
    def get_best_territories_to_defend(self, tid_list):
        tid_set = set(tid_list)

        # -------------------------
        # 1. Controlled continents
        # -------------------------
        controlled_conts = {}
        cont_troop_totals = {}

        for cont_name, cont_data in self.gs.map.conts.items():
            cont_tids = cont_data['trtys']
            if all(t in tid_set for t in cont_tids):
                # Find chokepoints within this continent
                chokepoints = [t for t in cont_tids if self.is_chokepoint(t)]
                controlled_conts[cont_name] = chokepoints
                cont_troop_totals[cont_name] = sum(
                    self.gs.map.territories[t].troops for t in cont_tids
                )

        # Sort controlled_conts by total troops descending
        controlled_conts = dict(
            sorted(controlled_conts.items(),
                key=lambda x: cont_troop_totals[x[0]],
                reverse=True)
        )

        # -------------------------
        # 2. Capital(s)
        # -------------------------
        my_capital_tid = None
        other_capitals = []

        for tid in tid_list:
            territory = self.gs.map.territories[tid]
            if territory.isCapital:
                if territory.name == self.capital:
                    my_capital_tid = tid
                else:
                    other_capitals.append(tid)

        # -------------------------
        # 3. Cities
        # -------------------------
        cities = [tid for tid in tid_list if self.gs.map.territories[tid].isCity]

        # -------------------------
        # 4. Other special categories
        # -------------------------
        megacities       = [tid for tid in tid_list if self.gs.map.territories[tid].isMegacity]
        transport_centers = [tid for tid in tid_list if self.gs.map.territories[tid].isTransportcenter]
        forts            = [tid for tid in tid_list if self.gs.map.territories[tid].isFort]
        halls            = [tid for tid in tid_list if self.gs.map.territories[tid].isHall]
        leylines         = [tid for tid in tid_list if self.gs.map.territories[tid].isLeyline]
        bureaus          = [tid for tid in tid_list if self.gs.map.territories[tid].isBureau]

        # -------------------------
        # 5. High value TIDs scoring
        # -------------------------
        scored = []
        for tid in tid_list:
            t = self.gs.map.territories[tid]
            points = 0

            if t.isMegacity:        points += 50
            if t.isTransportcenter: points += 40
            if t.isHall:            points += 50
            if t.isLeyline:         points += 15
            if t.isBureau:          points += 15
            if self.is_chokepoint(tid): points += 15

            if points > 0:
                scored.append((tid, points))

        scored.sort(key=lambda x: x[1], reverse=True)
        high_value_tids = [tid for tid, _ in scored]

        # -------------------------
        # 6. Build and return result
        # -------------------------
        return {
            "controlled_conts":   controlled_conts,
            "capital":            my_capital_tid,
            "capitals":           other_capitals,
            "cities":             cities,
            "megacities":         megacities,
            "transport_centers":  transport_centers,
            "forts":              forts,
            "halls":              halls,
            "leylines":           leylines,
            "bureaus":            bureaus,
            "high_value_tids":    high_value_tids,
        }