    # Initial Deployment
    def deploy_troops(self):
        locations = self.get_best_territories_to_defend(self.territories)
        total_deployable = self.deployable_amt

        capital_tid = locations["capital"]
        city_tids = locations["cities"]

        # --- Split deployable amount ---
        has_capital = capital_tid is not None
        has_cities = len(city_tids) > 0

        if has_capital and has_cities:
            capital_share = total_deployable // (1+len(city_tids))
            cities_share = total_deployable - capital_share
        elif has_capital and not has_cities:
            capital_share = total_deployable
            cities_share = 0
        elif has_cities and not has_capital:
            capital_share = 0
            cities_share = total_deployable
        else:
            return  # nothing to deploy to

        # --- Deploy to capital ---
        if has_capital:
            self.gs.map.territories[capital_tid].troops += capital_share

            self.gs.server.emit('selectionSoundFx', room=self.gs.lobby)

            self.gs.server.emit('troop_addition_display', {f'{capital_tid}': {'tid': capital_tid, 'number': capital_share},}, room=self.gs.lobby)
            self.gs.server.emit('update_trty_display',{capital_tid:{'troops':self.gs.map.territories[capital_tid].troops}}, room=self.gs.lobby)

        # --- Deploy equally to cities ---
        if has_cities:
            per_city = cities_share // len(city_tids)
            city_leftover = cities_share - (per_city * len(city_tids))

            for tid in city_tids:
                self.gs.map.territories[tid].troops += per_city

                self.gs.server.emit('troop_addition_display', { f'{tid}': {'tid': tid, 'number': per_city},}, room=self.gs.lobby)
                self.gs.server.emit('update_trty_display',{tid:{'troops':self.gs.map.territories[tid].troops}}, room=self.gs.lobby)

            # City leftover also goes to capital, or first city if no capital
            if city_leftover > 0:
                if has_capital:
                    self.gs.map.territories[capital_tid].troops += city_leftover

                    self.gs.server.emit('troop_addition_display', { f'{tid}': {'tid': tid, 'number': per_city},}, room=self.gs.lobby)
                    self.gs.server.emit('update_trty_display',{tid:{'troops':self.gs.map.territories[tid].troops}}, room=self.gs.lobby)
                else:
                    self.gs.map.territories[city_tids[0]].troops += city_leftover

                    self.gs.server.emit('troop_addition_display', { f'{tid}': {'tid': tid, 'number': per_city},}, room=self.gs.lobby)
                    self.gs.server.emit('update_trty_display',{tid:{'troops':self.gs.map.territories[tid].troops}}, room=self.gs.lobby)

            self.gs.server.emit('selectionSoundFx', room=self.gs.lobby)
        
        self.deployable_amt = 0

        if self.gs.GES.current_event == None:
            self.gs.GES.selected += 1

