    def get_attack_sequences(
        self,
        curr_list,
        targets,
        N=None,
        main_group=None,
        grudge_targets=None,
        minimize_offense=True,
        territorial_importance=None,
        other_player_stats=None,
        my_own_stats=None
    ):
        import heapq
        from collections import defaultdict

        grudge_targets         = grudge_targets or []
        main_group             = main_group or []
        targets                = targets or []
        territorial_importance = territorial_importance or {}

        my_ind   = my_own_stats.get('ind_lvl', 1)
        my_infra = my_own_stats.get('infra_lvl', 1)
        my_ley   = my_own_stats.get('num_ley', 0)

        player_set = set(curr_list)
        mg_set     = set(main_group)

        # ------------------------------------------------------------------ #
        #  HELPERS
        # ------------------------------------------------------------------ #

        def get_owner(tid):
            for pid, player in self.gs.players.items():
                if tid in player.territories:
                    return pid, player
            return None, None

        def easiness(tid):
            pid, owner = get_owner(tid)
            if owner is None:
                return 1.0
            ps = other_player_stats.get(pid, {})
            disparity = (
                (ps.get('ind_lvl',   0) - my_ind) +
                (ps.get('infra_lvl', 0) - my_infra)
            )
            ley_dis          = ps.get('num_ley', 0) - my_ley
            t                = self.gs.map.territories[tid]
            effective_troops = max(1, t.troops + ley_dis * 2)
            if disparity == 0:
                strength_mult = 1.0
            elif disparity < 0:
                strength_mult = 1.0 / abs(disparity)
            else:
                if effective_troops <= 3:
                    strength_mult = 1.0 + (disparity * 0.1)
                else:
                    strength_mult = 1.0 + (disparity * 0.5)
                    
            raw_easiness = effective_troops * strength_mult
    
            # Hard cap: single troop territory never costs more than 3
            # regardless of disparity — 3 troops can always overrun 1
            if t.troops <= 1:
                return min(raw_easiness, 3.0)
            elif t.troops <= 3:
                return min(raw_easiness, t.troops * 2.0)
            
            return raw_easiness

        def offense_points(tid):
            for pid, imp_dict in territorial_importance.items():
                if isinstance(imp_dict, tuple):
                    imp_dict = imp_dict[0]
                if tid in imp_dict:
                    return imp_dict[tid]
            return 0.0

        def is_grudge(tid):
            pid, _ = get_owner(tid)
            return pid in grudge_targets

        def tile_cost(tid):
            e = easiness(tid)
            o = offense_points(tid)
            g = 0 if is_grudge(tid) else 1
            if minimize_offense:
                return (e, o, g)
            else:
                return (g, -o, e)

        def neighbors_outside(tid):
            return [
                n for n in self.gs.map.territories[tid].neighbors
                if n not in player_set
            ]

        # ------------------------------------------------------------------ #
        #  CANDIDATE STARTING TILES
        # ------------------------------------------------------------------ #

        def candidate_starts(strict_mg=False):
            if strict_mg and mg_set:
                starts = [t for t in mg_set if t in player_set and neighbors_outside(t)]
                if starts:
                    starts.sort(key=lambda t: -self.gs.map.territories[t].troops)
                    return starts
                return None

            starts = [t for t in curr_list if neighbors_outside(t)]
            if mg_set:
                starts.sort(key=lambda t: (
                    0 if t in mg_set else 1,
                    -self.gs.map.territories[t].troops
                ))
            else:
                starts.sort(key=lambda t: -self.gs.map.territories[t].troops)
            return starts

        # ------------------------------------------------------------------ #
        #  STEINER TREE
        # ------------------------------------------------------------------ #

        def build_steiner_tree(starts, targets):
            target_set = set(targets)
            reached    = set()
            parent     = {}
            children   = defaultdict(list)
            in_tree    = set()  # starts EMPTY

            if minimize_offense:
                init_cost = (0.0, 0.0)
            else:
                init_cost = (0.0,)

            inf_cost = tuple(float('inf') for _ in init_cost)

            def add_costs(a, b):
                return tuple(x + y for x, y in zip(a, b))

            def step_cost(nb):
                if minimize_offense:
                    return (easiness(nb), offense_points(nb))
                else:
                    return (easiness(nb),)

            dist = {}
            heap = []

            sorted_starts = sorted(
                starts,
                key=lambda t: -self.gs.map.territories[t].troops
            )

            for rank, s in enumerate(sorted_starts):
                penalty    = tuple(rank * 0.5 for _ in init_cost)
                start_cost = penalty
                dist[s]    = start_cost
                parent[s]  = None  # None stops traceback naturally
                heapq.heappush(heap, (start_cost, s))

            while heap and len(reached) < len(target_set):
                cost, cur = heapq.heappop(heap)

                if cost > dist.get(cur, inf_cost):
                    continue

                if cur in target_set and cur not in reached:
                    reached.add(cur)

                    node = cur
                    while parent.get(node) is not None:
                        par = parent[node]
                        if node not in children[par]:
                            children[par].append(node)
                        if par in in_tree:
                            break
                        in_tree.add(par)
                        node = par

                for nb in self.gs.map.territories[cur].neighbors:
                    if nb in player_set:
                        continue
                    nb_cost = add_costs(cost, step_cost(nb))
                    if nb_cost < dist.get(nb, inf_cost):
                        dist[nb] = nb_cost
                        parent[nb] = cur
                        heapq.heappush(heap, (nb_cost, nb))

            return children, reached

        # ------------------------------------------------------------------ #
        #  TREE HELPERS
        # ------------------------------------------------------------------ #

        def get_subtree(node, children):
            result = [node]
            for child in children.get(node, []):
                result.extend(get_subtree(child, children))
            return result

        def tree_to_chain(node, children):
            kids = children.get(node, [])
            if not kids:
                return []
            if len(kids) == 1:
                child = kids[0]
                step  = [node, child, easiness(child)]
                rest  = tree_to_chain(child, children)
                if isinstance(rest, dict):
                    return [step, rest]
                else:
                    return [step] + rest
            else:
                kids.sort(
                    key=lambda k: sum(offense_points(t) for t in get_subtree(k, children)),
                    reverse=True
                )
                branches = []
                for k in kids:
                    step = [node, k, easiness(k)]
                    rest = tree_to_chain(k, children)
                    if isinstance(rest, dict):
                        branch = [step, rest]
                    elif isinstance(rest, list):
                        branch = [step] + rest
                    else:
                        branch = [step]
                    branches.append(branch)
                return {'split_from': node, 'branches': branches}

        # ------------------------------------------------------------------ #
        #  MAIN LOGIC — WITH TARGETS
        # ------------------------------------------------------------------ #

        all_sequences = []
        captured_set  = set(curr_list)

        if targets:
            if mg_set:
                mg_starts = candidate_starts(strict_mg=True)
                starts    = mg_starts if mg_starts else candidate_starts(strict_mg=False)
            else:
                starts = candidate_starts(strict_mg=False)

            if not starts:
                starts = candidate_starts(strict_mg=False)

            children, reached = build_steiner_tree(starts, targets)

            if reached:
                roots = {tid for tid in children if tid in player_set}

                for root in roots:
                    result = tree_to_chain(root, children)
                    if result:
                        all_sequences.append(result)

                def collect_captured(node):
                    if node not in player_set:
                        captured_set.add(node)
                    for child in children.get(node, []):
                        collect_captured(child)

                for root in roots:
                    collect_captured(root)

        # ------------------------------------------------------------------ #
        #  MAIN LOGIC — N TILES
        # ------------------------------------------------------------------ #

        else:
            captured_count = 0
            frontier       = []

            if mg_set:
                mg_starts = candidate_starts(strict_mg=True)
                starts    = mg_starts if mg_starts else candidate_starts(strict_mg=False)
            else:
                starts = candidate_starts(strict_mg=False)

            for s in starts:
                for nb in neighbors_outside(s):
                    if nb not in captured_set:
                        heapq.heappush(frontier, (tile_cost(nb), nb, s, s))

            children = defaultdict(list)
            roots    = set()

            while frontier and (N is None or captured_count < N):
                cost, tid, came_from, orig_start = heapq.heappop(frontier)

                if tid in captured_set:
                    continue

                captured_set.add(tid)
                captured_count += 1
                children[came_from].append(tid)

                if came_from in player_set:
                    roots.add(came_from)

                for nb in self.gs.map.territories[tid].neighbors:
                    if nb not in captured_set:
                        heapq.heappush(frontier, (tile_cost(nb), nb, tid, orig_start))

            for root in roots:
                result = tree_to_chain(root, children)
                if result:
                    all_sequences.append(result)

        # ------------------------------------------------------------------ #
        #  STATS
        # ------------------------------------------------------------------ #

        encountered_pids = set()
        own_set          = set(curr_list)

        for pid, player in self.gs.players.items():
            for trty in player.territories:
                if trty in captured_set and trty not in own_set:
                    encountered_pids.add(pid)

        strongest_player    = None
        strongest_score     = -float('inf')
        max_ind_disparity   = 0
        max_infra_disparity = 0
        max_ley_disparity   = 0

        for pid in encountered_pids:
            if pid not in other_player_stats:
                continue
            ps = other_player_stats[pid]

            ind_dis   = ps.get('ind_lvl',   0) - my_ind
            infra_dis = ps.get('infra_lvl', 0) - my_infra
            ley_dis   = ps.get('num_ley',   0) - my_ley
            score     = ind_dis + ley_dis

            if score > strongest_score:
                strongest_score  = score
                strongest_player = pid

            if ind_dis   > max_ind_disparity:   max_ind_disparity   = ind_dis
            if infra_dis > max_infra_disparity: max_infra_disparity = infra_dis
            if ley_dis   > max_ley_disparity:   max_ley_disparity   = ley_dis

        stats_summary = {
            'strongest_player':    strongest_player,
            'max_ind_disparity':   max_ind_disparity,
            'max_infra_disparity': max_infra_disparity,
            'max_ley_disparity':   max_ley_disparity,
        }

        return all_sequences, stats_summary